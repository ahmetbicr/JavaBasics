package com.syntax.class01;

public class PracticeVariables {
	
	public static void main(String[] args) {
		
		// byte, short, int, long, float, double, char, boolean
		
		byte num0 = 15;					// -128 to 127
		short num1 = 1500;				// -32768 to 32767
		int num2 = 65746;				// -2147483648 to 2147483647
		long num3= 7627384623L;			// l or L
		float num4 = 798.5F;			// f or F
		double num5 = 7419823.123;
		char ch = '$';
		boolean question = true;			// true or false
		
		System.out.println(num0);
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		System.out.println(num5);
		System.out.println(ch);
		System.out.println(question);
	}
}
